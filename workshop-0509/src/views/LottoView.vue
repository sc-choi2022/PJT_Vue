<template>
  <div>
    <h1>로또</h1>
    <button @click="getLottoNumber">Get Lucky Numbers</button>
    <p>{{ numbers }}</p>
  </div>
</template>

<script>
import _ from 'lodash'
export default {
  name: 'LottoView',
  data: function(){
    return {
      numbers: ''
    }
  },
  methods: {
    getLottoNumber(){
      this.numbers = _.sampleSize(_.range(1, 46), this.$route.params.lottoNum)
    }
  }
}
</script>

